#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*str;

	i = ft_strlen(s1);
	str = malloc(i + 1);
	if (!str)
		return (NULL);
	ft_memcpy(str, s1, i + 1);
	return (str);
}
